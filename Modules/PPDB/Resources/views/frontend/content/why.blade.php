            </div>
            <div class="step">
                <h3>2. Pendaftaran Online</h3>
                <p>Klik menu "Registrasi" dan pilih "Pendaftaran Santri Baru". Isi formulir pendaftaran dengan lengkap dan benar.</p>
            </div>
            <div class="step">
                <h3>3. Verifikasi Data</h3>
                <p>Setelah mengisi formulir, lakukan verifikasi data yang telah diisi. Pastikan semua informasi sudah benar dan lengkap.</p>
            </div>
            <div class="step">
                <h3>4. Pembayaran Biaya Pendaftaran</h3>
                <p>Lakukan pembayaran biaya pendaftaran sesuai dengan petunjuk di website. Simpan bukti pembayaran untuk keperluan verifikasi.</p>
            </div>
            <div class="step">
                <h3>5. Konfirmasi Pendaftaran</h3>
                <p>Kirim bukti pembayaran melalui fitur yang tersedia di website atau email ke bagian administrasi. Tunggu konfirmasi status pendaftaran.</p>
            </div>
            <div class="step">
                <h3>6. Tes Seleksi</h3>
                <p>Ikuti tes seleksi yang terdiri dari tes akademik dan wawancara. Jadwal tes akan diberitahukan melalui email atau pengumuman di website.</p>
            </div>
            <div class="step">
                <h3>7. Pengumuman Hasil Seleksi</h3>
                <p>Hasil seleksi akan diumumkan di website resmi pondok pesantren dan melalui email. Calon santri yang lulus seleksi akan menerima surat penerimaan resmi.</p>
            </div>
            <div class="step">
                <h3>8. Daftar Ulang</h3>
                <p>Lakukan proses daftar ulang dengan mengisi formulir daftar ulang dan melengkapi dokumen yang diperlukan. Bayar biaya pendidikan dan administrasi lainnya sesuai dengan ketentuan yang berlaku.</p>
            </div>
            <div class="step">
                <h3>9. Masuk Asrama</h3>
                <p>Setelah semua proses pendaftaran selesai, calon santri baru dapat mulai masuk asrama pada tanggal yang telah ditentukan. Ikuti orientasi santri baru untuk mengenal lingkungan pondok pesantren dan aturan-aturan yang berlaku.</p>
            </div>
            <div class="step">
                <h3>10. Mulai Pembelajaran</h3>
